package ch02;

public class VarEx3 {

	public static void main(String[] args) {
		boolean power = false;
		
		byte b = 127; // - 128 ~ 127
		
		int oct = 010;  // 8진수, 10진수로 8
		int hex = 0x10; // 16진수, 10진수 16
		
		long l = 10_000_000_000L;
		
		float f = 3.14f;
		double d = 3.14f;
		
		System.out.println(10.);
		System.out.println(.10);
		System.out.println(l);
		System.out.println(10f);
		System.out.println(1e3);
	}
}
