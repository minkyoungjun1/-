package ch04;

import java.util.Scanner;

public class FlowEx16 {

	public static void main(String[] args) {
		
		// i=1,2,3,4,5번 {} 안의 내용을 반복
		for(int i=1; i<=5; i++) {
			// System.out.print("**********");
			for(int j=1; j<=5; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
	}

}
